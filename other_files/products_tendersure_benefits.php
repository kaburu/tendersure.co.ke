          <p align="justify" class="pagetitle">Tendersure Benefits </p>
     
          <div align="justify"><span class="small-text2" style="font-weight: bold; color: #CC0033">OUR MOTIVATION
          as entrepreneurs        </span>        </div>
        <p align="justify" class="small-text2">Being  an entrepreneur inevitably leads to taking part in tenders. Or at least it should. Lots   of businesses don&rsquo;t even bother to participate because of the red tape   involved and the poor image of the decision making process. Not a day   goes by without a tender scandal in the newspapers. The 21st century is a   great time to get rid of all the paperwork and digitise the tender   process in a state of the art way, making sure that tenders result in   what they are meant for: reporting the best bidder for the job. And that   is seldom the friend of a friend.</p>
         <div align="justify"><span class="small-text2" style="color: #CC0033; font-weight: bold">BENEFITS OF
           electronically codifying tenders         </span>          </div>
        <p align="justify" class="small-text2"> The tender is re-worked from its   issued paper format into an electronic form where potential bidders are   able to submit their information online in the comfort of their own   offices. A simple web browser is sufficient for the bidder to access the   Tendersure system and submit information, no additional software needs   to be purchased in order to access or use Tendersure. QED Solutions  recommends the use of the Firefox browser <a href="http://www.mozilla.com/en-US/firefox/personal.html" target="_blank">(http://www.mozilla.com/en-US/firefox/personal.html)</a> with Tendersure, but supports other browsers as well. All bidder data   is stored in an encrypted database locked to the bidder&rsquo;s user name and   password.</p>
         <div align="justify"><span class="small-text2" style="font-weight: bold; color: #CC0033">BENEFITS OF
           making tenders available on a server portal         </span>          </div>
        <p align="justify" class="small-text2">New tenders can be issued through the   same server portal used to collect the tender data from bidders. The   tender issuer can communicate more directly with potential bidders by   allowing them to register on the Tendersure system and select which   tender category notices they wish to receive. The bidders can then   tailor their own information feed without requiring additional labour or   costs to monitor bulletin boards and traditional tender notice boards.   The bidders will receive notices into their personal (elected) email   inbox.</p>
         <div align="justify"><span class="small-text2" style="font-weight: bold; color: #CC0033">BENEFITS OF
           a System communicating to bidders and clients         </span>          </div>
        <p align="justify" class="small-text2"> Tendersure will alert potential   bidders via email and SMS of the issue of a new tender in their   registered category. Bidders can then download the new tender from the   system and decide whether or not they wish to bid on the new tender.   This decision on whether to bid or not can now be made the same day that   the tender is released and not have to wait for the tender notice to be   issued or for the tender to be physically retrieved.</p>
        <p align="justify" class="small-text2"> Once a decision on   the granting of a tender has been made, the same server portal is again   used to communicate the decision to all the bidding parties as well as   other interested parties. Supporting documentation is provided to   bidding parties comparing their bid to the winning bid and the areas   where the winning bid is superior to their own are highlighted. This   allows bidders to identify the areas where their own business needs to   improve in order to win future tenders, as well as provide transparency   that the best bid has actually won.</p>
        <p align="justify" class="small-text2">Finally, the Tendersure system provides   feedback during the bidding process; informing bidders of information   missing from the bid that would cause the bid to be marked   non-responsive (incomplete). The bidder can create custom messages based   on time and date triggers to alert them to bid milestones in addition   to the automated &lsquo;bid incomplete&rsquo; messages. Email and SMS&rsquo;es (at the   bidder&rsquo;s discretion) are the medium of communication.</p>
        <div align="justify"><span class="small-text2" style="font-weight: bold; color: #CC0033">BENEFITS OF
          Evaluating &amp; Ranking </span>        </div>
        <p align="justify" class="small-text2">After the time lock of the tender has   passed, the Tendersure system retrieves the bid information and   evaluates the bids according to the pre-defined criteria. This saves   manpower on the side of the tendering party and provides assurance of an   equitable, auditable evaluation for all bidding parties. The tender   bids are then ranked and submitted to the decision making body in the   form of a compact report with all the bid raw data attached. The winning   bid must now be selected from the amongst the best bids, severely   reducing the benefit gained from illicit influence. Any decision too far   outside the norm is instantly flagged to a watchdog body for   investigation.</p>
        <div align="justify"><span class="small-text2" style="color: #CC0033; font-weight: bold">BENEFITS OF
          Reporting Results        </span>        </div>
        <p align="justify" class="small-text2">The results of the automated bid   evaluation are provided in a report format (with raw data and ranking   criteria attached) where the best bids are clearly identified and   elevated above the non-responsive and undeserving bids. A tender   decision committee is now able to apply their minds to evaluating the   bids, rather than waste time with repetitive and mechanical vetting and   calculation. All recipients of the report can be confident of receiving   consistent, coherent and unbiased data from which to draw conclusions.   Once a decision is reached, all bidding parties will be informed of the   decision automatically and provided with concrete information as to why   their bids were unsuccessful, without the need for the tender issuer to   divert additional resources. Transparency and communication between the   tender issuer and bidder is enhanced without the need for additional   spending.</p>
        <div align="justify"><span class="small-text2" style="font-weight: bold; color: #CC0033">BENEFITS OF
          Archiving &amp; Relating </span>        </div>
        <p align="justify" class="small-text2">The digital storage and off site backup   of tender data will ensure an audit trail of any tender for the   foreseeable future. Any investigation will be able to access all tender   data at a central location, which can also be made open to the general   public. Further, the centralised storage of tender data allows for the   analysis and evaluation of trends. Spending differences across provinces   can be identified and investigated, allowing for the distribution of   best practice and highlighting of illicit business practices such as   price fixing.</p>
